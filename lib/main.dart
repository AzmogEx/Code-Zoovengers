void main() async {
  WidgetsFlutterBinding.ensureInitialized();
  GoRouter.optionURLReflectsImperativeAPIs = true;
  usePathUrlStrategy();

  await FlutterFlowTheme.initialize();

  final appState = FFAppState(); // Initialize FFAppState
  await appState.initializePersistedState();

  runApp(ChangeNotifierProvider(
    create: (context) => appState,
    child: const MyApp(),
  ));
}

class MyApp extends StatefulWidget {
  const MyApp({super.key});

  // This widget is the root of your application.
  @override
  State<MyApp> createState() => _MyAppState();

  static _MyAppState of(BuildContext context) =>
      context.findAncestorStateOfType<_MyAppState>()!;
}

class _MyAppState extends State<MyApp> {
  Locale? _locale;

  ThemeMode _themeMode = FlutterFlowTheme.themeMode;

  late AppStateNotifier _appStateNotifier;
  late GoRouter _router;

  bool displaySplashImage = true;

  @override
  void initState() {
    super.initState();

    _appStateNotifier = AppStateNotifier.instance;
    _router = createRouter(_appStateNotifier);

    Future.delayed(const Duration(milliseconds: 1000),
        () => setState(() => _appStateNotifier.stopShowingSplashImage()));
  }

  void setLocale(String language) {
    setState(() => _locale = createLocale(language));
  }

  void setThemeMode(ThemeMode mode) => setState(() {
        _themeMode = mode;
        FlutterFlowTheme.saveThemeMode(mode);
      });

  @override
  Widget build(BuildContext context) {
    return MaterialApp.router(
      title: 'Zoovengers',
      localizationsDelegates: const [
        FFLocalizationsDelegate(),
        GlobalMaterialLocalizations.delegate,
        GlobalWidgetsLocalizations.delegate,
        GlobalCupertinoLocalizations.delegate,
      ],
      locale: _locale,
      supportedLocales: const [
        Locale('fr'),
        Locale('en'),
        Locale('es'),
      ],
      theme: ThemeData(
        brightness: Brightness.light,
        useMaterial3: false,
      ),
      darkTheme: ThemeData(
        brightness: Brightness.dark,
        useMaterial3: false,
      ),
      themeMode: _themeMode,
      routerConfig: _router,
    );
  }
}

class NavBarPage extends StatefulWidget {
  const NavBarPage({super.key, this.initialPage, this.page});

  final String? initialPage;
  final Widget? page;

  @override
  _NavBarPageState createState() => _NavBarPageState();
}

/// This is the private State class that goes with NavBarPage.
class _NavBarPageState extends State<NavBarPage> {
  String _currentPageName = 'Accueil';
  late Widget? _currentPage;

  @override
  void initState() {
    super.initState();
    _currentPageName = widget.initialPage ?? _currentPageName;
    _currentPage = widget.page;
  }

  @override
  Widget build(BuildContext context) {
    final tabs = {
      'Accueil': const AccueilWidget(),
      'Decryptek': const DecryptekWidget(),
      'Scan': const ScanWidget(),
      'Enigmes': const EnigmesWidget(),
      'password': const PasswordWidget(),
    };
    final currentIndex = tabs.keys.toList().indexOf(_currentPageName);

    return Scaffold(
      body: _currentPage ?? tabs[_currentPageName],
      bottomNavigationBar: GNav(
        selectedIndex: currentIndex,
        onTabChange: (i) => setState(() {
          _currentPage = null;
          _currentPageName = tabs.keys.toList()[i];
        }),
        backgroundColor: const Color(0xFF344D59),
        color: Colors.white,
        activeColor: const Color(0xFFF46969),
        tabBackgroundColor: const Color(0x00000000),
        tabBorderRadius: 100.0,
        tabMargin: const EdgeInsetsDirectional.fromSTEB(0.0, 0.0, 0.0, 0.0),
        padding: const EdgeInsets.all(16.0),
        gap: 0.0,
        mainAxisAlignment: MainAxisAlignment.spaceAround,
        duration: const Duration(milliseconds: 500),
        haptic: false,
        tabs: [
          GButton(
            icon: Icons.home,
            text: FFLocalizations.of(context).getText(
              '23vakbx8' /* Accueil */,
            ),
          ),
          GButton(
            icon: Icons.key,
            text: FFLocalizations.of(context).getText(
              'f2y9mbrm' /* Decryptek */,
            ),
          ),
          GButton(
            icon: Icons.qr_code,
            text: FFLocalizations.of(context).getText(
              '2t6ado82' /* Scan */,
            ),
          ),
          GButton(
            icon: Icons.castle,
            text: FFLocalizations.of(context).getText(
              'x7pnqojq' /* Enigmes */,
            ),
            iconSize: 24.0,
          ),
          GButton(
            icon: Icons.password,
            text: FFLocalizations.of(context).getText(
              'l1yiy5pz' /* Final */,
            ),
            iconSize: 24.0,
          )
        ],
      ),
    );
  }
}
